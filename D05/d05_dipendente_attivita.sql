-- create tables
create table d05_dipendente (
    id                             number generated by default on null as identity 
                                   constraint d05_dipendente_id_pk primary key,
    dipendente                     varchar2(9 char)
                                   constraint d05_dipendente_dipendente_unq unique not null,
    tariffa                        number not null
)
;

create table d05_cliente (
    id                             number generated by default on null as identity 
                                   constraint d05_cliente_id_pk primary key,
    cliente                        varchar2(10 char)
                                   constraint d05_cliente_cliente_unq unique not null
)
;

create table d05_commessa (
    id                             number generated by default on null as identity 
                                   constraint d05_commessa_id_pk primary key,
    commessa                       varchar2(6 char)
                                   constraint d05_commessa_commessa_unq unique not null
)
;

create table d05_attivita (
    id                             number generated by default on null as identity 
                                   constraint d05_attivita_id_pk primary key,
    dipendente_id                  number
                                   constraint d05_attivita_dipendente_id_fk
                                   references d05_dipendente on delete cascade not null,
    cliente_id                     number
                                   constraint d05_attivita_cliente_id_fk
                                   references d05_cliente on delete cascade not null,
    commessa_id                    number
                                   constraint d05_attivita_commessa_id_fk
                                   references d05_commessa on delete cascade not null,
    data                           date not null,
    ore                            number not null
)
;

-- table index
create index d05_attivita_i1 on d05_attivita (cliente_id);
create index d05_attivita_i112 on d05_attivita (commessa_id);
create index d05_attivita_i123 on d05_attivita (dipendente_id);

